'''
    check max number using if else condition
'''

print("Check max and min number")

num1 = int(input("Enter num1: "))
num2 = int(input("Enter num2: "))


if num1 == num2:
    print("Num1 and Num2 are equal")
elif num1 > num2:
    print("Num1 is max Number")
else:
    print("Num2 is max Number")
