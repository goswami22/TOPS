'''
    string slicing
'''
s = "Python Programing Language"

print(s[:10])

print(s[10:])

print(s[-25:-15])

print(s[3:18:3])

print(s[3:18:-3])

print(s[-5:-20:3])

print(s[-5:-20:-3])

print(s[20:5:-4])

print(s[-1: -3])

print(s[-1: : -3])

